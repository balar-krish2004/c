#include <stdio.h> //standard input-output header file library

int main()
{

    // datatype int float double char


    // datatype  printf
    // int %d
    // float %f 
    // double %lf 
    // char %c 
    int num = 10;
    printf("num=%d\n", num);

    float Price = 10.45;
    printf("Price=%f\n", Price);

    double Product_Price = 10.45678;
    printf("Product_Price=%lf\n", Product_Price);

    char A = 's';
    printf("A=%c\n", A);



    return 0;
}
